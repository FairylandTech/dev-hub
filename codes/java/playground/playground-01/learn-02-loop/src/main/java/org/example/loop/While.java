package org.example.loop;

/**
 * @author Lionel Johnson
 */
public class While {

    /*
     * 利用 while 循环打印 1-100
     * */

    public static void main(String[] args) {

        int i = 1;

        while (i < 101) {
            System.out.println(i);
            i++;
        }

    }

}
